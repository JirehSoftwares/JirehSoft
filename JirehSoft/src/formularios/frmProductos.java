    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Validaciones
        if (txtIdProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un ID");
            txtIdProducto.requestFocusInWindow();
            return;
        }
        if (txtDescripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar la descripcion");
            txtDescripcion.requestFocusInWindow();
            return;
        }

        if (txtPrecio.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el precio");
            txtPrecio.requestFocusInWindow();
            return;
        }
        if (cmbCategoria.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe Seleccionar una Categoria");
            cmbCategoria.requestFocusInWindow();
            return;
        }

        if (!Utilidades.isNumeric(txtPrecio.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el precio en numeros validos");
            txtPrecio.requestFocusInWindow();
            return;
        }
        int precio = Integer.parseInt(txtPrecio.getText());
        if (precio <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el precio mayor que cero");
            txtPrecio.requestFocusInWindow();
            return;
        }
        //Si es nuevo validamos que no exista
        int pos = misDatos.posicionProducto(txtIdProducto.getText());
        if (nuevo) {
            if (pos != -1) {
                JOptionPane.showMessageDialog(rootPane, "Producto ya existe");
                txtIdProducto.requestFocusInWindow();
                return;
            }
        } else {
            if (pos == -1) {
                JOptionPane.showMessageDialog(rootPane, "Producto no existe");
                txtIdProducto.requestFocusInWindow();
                return;
            }
        }

        //Creamos el objeto producto y lo agregamos a datos
        Producto miProducto = new Producto(
                txtIdProducto.getText(),
                txtDescripcion.getText(),
                precio,
                cmbCategoria.getSelectedIndex());
        String msg;
        if (nuevo) {
            msg = misDatos.agregarProducto(miProducto);
            nuevo = false;
        } else {
            msg = misDatos.modificarProducto(miProducto, pos);
        }

        JOptionPane.showMessageDialog(rootPane, msg);

        //Activa Botones desactiva campos
        btnInicio.setEnabled(true);
        btnSiguiente.setEnabled(true);
        btnAnterior.setEnabled(true);
        btnFinal.setEnabled(true);
        btnNuevo.setEnabled(true);
        btnEditar.setEnabled(true);
        btnBuscar.setEnabled(true);
        btnSalir.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnBorrar.setEnabled(true);
        txtIdProducto.setEnabled(false);
        txtDescripcion.setEnabled(false);
        txtPrecio.setEnabled(false);
        cmbCategoria.setEnabled(false);

        llenarTabla();

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (nuevo) {
            proAct = 0;
            mostrarRegistro();
            btnInicio.setEnabled(true);
            btnSiguiente.setEnabled(true);
            btnAnterior.setEnabled(true);
            btnFinal.setEnabled(true);
            btnNuevo.setEnabled(true);
            btnEditar.setEnabled(true);
            btnBuscar.setEnabled(true);
            btnSalir.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnBorrar.setEnabled(true);
            txtIdProducto.setEnabled(false);
            txtDescripcion.setEnabled(false);
            txtPrecio.setEnabled(false);
            cmbCategoria.setEnabled(false);

            nuevo = false;
            return;
        }
        int rta = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de borrar el registro?");
        if (rta != 0) {
            mostrarRegistro();
            btnInicio.setEnabled(true);
            btnSiguiente.setEnabled(true);
            btnAnterior.setEnabled(true);
            btnFinal.setEnabled(true);
            btnNuevo.setEnabled(true);
            btnEditar.setEnabled(true);
            btnBuscar.setEnabled(true);
            btnSalir.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnBorrar.setEnabled(true);
            txtIdProducto.setEnabled(false);
            txtDescripcion.setEnabled(false);
            txtPrecio.setEnabled(false);
            cmbCategoria.setEnabled(false);
            return;
        }
        String msg;
        msg = misDatos.BorrarProducto(proAct);
        JOptionPane.showMessageDialog(rootPane, msg);
        proAct = 0;
        mostrarRegistro();
        btnInicio.setEnabled(true);
        btnSiguiente.setEnabled(true);
        btnAnterior.setEnabled(true);
        btnFinal.setEnabled(true);
        btnNuevo.setEnabled(true);
        btnEditar.setEnabled(true);
        btnBuscar.setEnabled(true);
        btnSalir.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnBorrar.setEnabled(true);
        txtIdProducto.setEnabled(false);
        txtDescripcion.setEnabled(false);
        txtPrecio.setEnabled(false);
        cmbCategoria.setEnabled(false);
        llenarTabla();
    }//GEN-LAST:event_btnBorrarActionPerformed

